    @classmethod
    def t(cls, key):
        """Gets translation for a given key. If missing, falls back to English."""
        current_dict = TRANSLATIONS.get(cls._lang, TRANSLATIONS["en"])
        return current_dict.get(key, TRANSLATIONS["en"].get(key, key))

